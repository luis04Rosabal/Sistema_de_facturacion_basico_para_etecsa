  public void showid(){
       DefaultComboBoxModel<Factura>facturas = new DefaultComboBoxModel<>();
       for(Factura f : funcion.getFacturas()){
           facturas.addElement(f);
       }
       jComboBox1.setModel(facturas);
  }
  
  
   public void rellenar(Factura f){
       jTextField1.setText(f.getIdFactura());
       jTextField2.setText(f.getIdCliente());
       jTextField3.setText(String.valueOf(f.getMonto()));
       jTextField4.setText(String.valueOf(f.getResto()));
       
      
   }
