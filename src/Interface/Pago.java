      public void mostrar(){
          DefaultComboBoxModel<Factura>factura = new DefaultComboBoxModel<>();
          for(Factura f : funcion.getFacturas()){
              factura.addElement(f);
             }
          jComboBox1.setModel(factura);
          }
      public void rellenar(Factura f){
          jTextField1.setText(f.getIdFactura());
       jTextField2.setText(String.valueOf(f.getMonto())); 
      }

