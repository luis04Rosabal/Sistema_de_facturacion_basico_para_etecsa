    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       Factura f = (Factura)jComboBox1.getModel().getSelectedItem();
       double monto = Double.parseDouble(jTextField2.getText());
       double rest = f.getMonto() - monto;
       if(f.getMonto()==monto){
           f.setEstado(true);
           f.setResto(rest);
           f.setPago(monto);
           JOptionPane.showMessageDialog(rootPane,"pago realizado con exito");
       }else{
           f.setResto(rest);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

