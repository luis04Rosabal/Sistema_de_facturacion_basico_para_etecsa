    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
        Facturas f = (Facturas)jComboBox1.getModel().getSelectedItem();
        String idFactura = jTextField1.getText();
        String idCliente = jTextField2.getText();
        double monto = Double.parseDouble(jTextField3.getText());
        Date fechaEmision = (Date)jSpinner1.getModel().getValue();
        f.setIdCliente(idCliente);
        f.setIdfactura(idFactura);
        f.setFechaEmision(fechaEmision);
        f.setMontoFactura(monto);
        JOptionPane.showMessageDialog(rootPane,"Factura actualizada con exito");
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane,"Datos introducidos incorrectos pruebe de nuevo");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

