     public ActualizarFactura( Etecsa etecsa) {
        this(null,true);
        this.etecsa=etecsa;
         DefaultComboBoxModel<Facturas>facturas = new DefaultComboBoxModel<>();
         for(Facturas f : etecsa.getLista()){
             facturas.addElement(f);
         }
         jComboBox1.setModel(facturas);
         Facturas f = etecsa.getLista().get(0);
         rellenar(f);
    }

    public void rellenar(Facturas f){
        jTextField1.setText(f.getIdfactura());
        jTextField2.setText(f.getIdCliente());
        jTextField3.setText(String.valueOf(f.getMontoFactura()));
         
    }
